RTC_HandleTypeDef hrtc;

/* RTC init function */
void MX_RTC_Init(unsigned ms_per_tick)
{
  RTC_TimeTypeDef sTime = {0};
  RTC_DateTypeDef DateToUpdate = {0};

  /** Initialize RTC Only 
  */
  hrtc.Instance = RTC;
  switch (ms_per_tick) {
	  case 250:  hrtc.Init.AsynchPrediv = 0x1FFF; break; // 4 Hz RTC
	  case 500:  hrtc.Init.AsynchPrediv = 0x3FFF; break; // 2 Hz RTC
	  case 2000: hrtc.Init.AsynchPrediv = 0xFFFF; break; // 0.5 Hz RTC
	  default: hrtc.Init.AsynchPrediv = RTC_AUTO_1_SECOND;
  }
  //hrtc.Init.OutPut = RTC_OUTPUTSOURCE_ALARM;
  hrtc.Init.OutPut = RTC_OUTPUTSOURCE_NONE; // To release PC13 for GPIO. CubeMX bug work-around.
  if (HAL_RTC_Init(&hrtc) != HAL_OK)
  {
    Error_Handler();
  }

  /* USER CODE BEGIN Check_RTC_BKUP */
    
  /* USER CODE END Check_RTC_BKUP */

  /** Initialize RTC and set the Time and Date 
  */
  sTime.Hours = 0;
  sTime.Minutes = 0;
  sTime.Seconds = 0;

  if (HAL_RTC_SetTime(&hrtc, &sTime, RTC_FORMAT_BIN) != HAL_OK)
  {
    Error_Handler();
  }
  DateToUpdate.WeekDay = RTC_WEEKDAY_MONDAY;
  DateToUpdate.Month = RTC_MONTH_JANUARY;
  DateToUpdate.Date = 1;
  DateToUpdate.Year = 0;

  if (HAL_RTC_SetDate(&hrtc, &DateToUpdate, RTC_FORMAT_BIN) != HAL_OK)
  {
    Error_Handler();
  }

}

